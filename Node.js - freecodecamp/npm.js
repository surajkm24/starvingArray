// npm - node package manager
// global command , comes with node
// Used for installing packages for doing projects
// Packages are just folders and files which contains JS codes for doing a particular task or a set of tasks.
//Modules , packages and dependencies are used interchangeably.

// local dependency - use it only in a particular project
// npm i <packagename>

// global dependency - use it in any project
// npm i -g <packagename>

// package.json - manifest file (stores important info about project)
// manula approach (create package.json in the root, properties etc.)
// npm init (step by step, press enter to skip)
// npm init -y (everything default)

// Node-modules --> all packages are stored in a folder named node-modules.
//It is created by node;

// Nodemon --> package
// It just watches our files and if detects any changes then restarts our file

// devDependencies --> packages we use for developing a app;
// npm i <packagename> --save-dev || -D

